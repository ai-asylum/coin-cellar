// Seed Coin Cellar's wares into the game's own cooking backend's
// `ingredients` table. Two modes:
//
//   node scripts/seed-cooking-ingredients.mjs --sql
//     Print the upsert as SQL — used to generate the migration
//     (supabase/migrations/0002_coin_cellar_ingredients.sql).
//
//   SUPABASE_SERVICE_ROLE_KEY=... node scripts/seed-cooking-ingredients.mjs
//     Upsert straight through the REST API. Needs the service-role key
//     (ingredients RLS only lets service_role write); the anon key that ships
//     in the client can't do this.
//
// Idempotent either way: rows are keyed on the cc_-prefixed slug, so
// re-running just refreshes names/descriptions.
import { INGREDIENTS } from "../src/cooking/ingredients.js";

const SUPABASE_URL = "https://oedoqjgqdjdnxxhdslcr.supabase.co";

// The two START_INV items (game.js) are the flagged starters; the rest of the
// catalogue are plain base rows. starter_order sits above the restaurant
// game's own starters (1..12) so the two orderings never interleave.
const rows = INGREDIENTS.map((ing, i) => ({
  slug: ing.slug,
  name: ing.name,
  emoji: ing.emoji,
  category: ing.category,
  description: ing.description,
  is_starter: ing.starter,
  starter_order: ing.starter ? 101 + INGREDIENTS.filter((x) => x.starter).indexOf(ing) : null,
}));

if (process.argv.includes("--sql")) {
  const q = (s) => (s === null ? "null" : `'${String(s).replace(/'/g, "''")}'`);
  const values = rows
    .map(
      (r) =>
        `  (${q(r.slug)}, ${q(r.name)}, ${q(r.emoji)}, ${q(r.category)}, ${q(r.description)}, ${r.is_starter}, ${r.starter_order ?? "null"})`
    )
    .join(",\n");
  console.log(`-- Coin Cellar's findable items as base ingredients. The coin-cellar game's
-- cooking view (coin-cellar/src/cooking/) runs against this same project;
-- its tray serves these rows' slugs (cc_-prefixed so they can never collide
-- with this game's own catalogue). The two is_starter rows are coin-cellar's
-- START_INV (what a fresh save carries); starter_order 101+ keeps them clear
-- of the restaurant's own starter ordering.
-- Generated by coin-cellar/scripts/seed-cooking-ingredients.mjs --sql.
insert into ingredients (slug, name, emoji, category, description, is_starter, starter_order)
values
${values}
on conflict (slug) do update set
  name = excluded.name,
  emoji = excluded.emoji,
  category = excluded.category,
  description = excluded.description,
  is_starter = excluded.is_starter,
  starter_order = excluded.starter_order;`);
  process.exit(0);
}

const key = process.env.SUPABASE_SERVICE_ROLE_KEY;
if (!key) {
  console.error(
    "SUPABASE_SERVICE_ROLE_KEY not set. Grab it with\n" +
      "`supabase projects api-keys --project-ref oedoqjgqdjdnxxhdslcr`, or run\n" +
      "with --sql and apply the migration via `supabase db push` instead."
  );
  process.exit(1);
}

const res = await fetch(`${SUPABASE_URL}/rest/v1/ingredients?on_conflict=slug`, {
  method: "POST",
  headers: {
    apikey: key,
    Authorization: `Bearer ${key}`,
    "Content-Type": "application/json",
    Prefer: "resolution=merge-duplicates,return=representation",
  },
  body: JSON.stringify(rows),
});

if (!res.ok) {
  console.error(`Upsert failed: ${res.status} ${await res.text()}`);
  process.exit(1);
}
const data = await res.json();
console.log(`Upserted ${data.length} ingredients:`);
for (const r of data) console.log(`  ${r.emoji}  ${r.slug} — ${r.name} (${r.category})${r.is_starter ? " [starter]" : ""}`);
